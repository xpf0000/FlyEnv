import type { UserConfig } from 'vite'
import { defineConfig } from 'vite'
import vue from '@vitejs/plugin-vue'
import * as path from 'path'
import vueJsx from '@vitejs/plugin-vue-jsx'
import wasm from 'vite-plugin-wasm'
import monacoEditorPlugin from 'vite-plugin-monaco-editor'

const renderPath = path.resolve(__dirname, '../src/render/')
const sharePath = path.resolve(__dirname, '../src/shared/')

console.log('__dirname: ', __dirname)
console.log('renderPath: ', renderPath)
console.log('sharePath: ', sharePath)

const config: UserConfig = {
  base: './',
  plugins: [monacoEditorPlugin({}), wasm(), vue(), vueJsx()],
  assetsInclude: ['**/*.node'],
  optimizeDeps: {
    esbuildOptions: {
      target: 'esnext',
      supported: {
        'top-level-await': true
      }
    },
    exclude: ['fsevents']
  },
  root: __dirname,
  resolve: {
    alias: {
      '@': renderPath,
      '@shared': sharePath,
      '@web': __dirname
    }
  },
  css: {
    // CSS preprocessor
    preprocessorOptions: {
      scss: {
        // Import var.scss so that the variables defined in var.scss can be used globally
        // Add a semicolon at the end of the import path
        additionalData: '@import "@/components/Theme/Variables.scss";'
      }
    }
  }
}

const serveConfig: UserConfig = {
  server: {
    port: 5000,
    open: true,
    hmr: true,
    host: '0.0.0.0'
  },
  ...config
}

const buildConfig: UserConfig = {
  build: {
    outDir: '../dist/web',
    assetsDir: 'static',
    target: 'esnext',
    rollupOptions: {
      input: {
        main: path.resolve(__dirname, './index.html')
      },
      output: {
        entryFileNames: 'static/js/[name].[hash].js',
        chunkFileNames: 'static/js/[name].[hash].js',
        assetFileNames: 'static/[ext]/[name].[hash].[ext]',
        manualChunks(id) {
          console.log('id: ', id)
          if (id.includes('node_modules')) {
            return id.toString().split('node_modules/')[1].split('/')[0].toString()
          }
          return undefined
        }
      }
    },
    minify: 'terser',
    terserOptions: {
      compress: {
        drop_console: true,
        drop_debugger: true
      }
    }
  },
  ...serveConfig
}

export default defineConfig(buildConfig)
